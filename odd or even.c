 #include<stdio.h>
 void main()
  {
    int n;
    printf("enter the value of the n\n");
    scanf("%d",&n);

    if(n%2!=0)
      {
        printf("The entered number is ODD");
      }
      else
      {
        printf("The entered number is EVEN");
      }
  }
